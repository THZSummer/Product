#!/bin/bash
# ============================================================================
# Thread-MG 脚本公共库
# ============================================================================
# 提供统一的颜色定义、日志函数、工具函数
#
# 用法：
#   source "$(dirname "$0")/lib.sh"
#
# 示例：
#   log_info "这是一条信息"
#   log_success "操作成功"
#   log_warning "这是一条警告"
#   log_error "发生错误"
# ============================================================================

# ============================================================================
# 颜色定义
# ============================================================================
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
BOLD='\033[1m'
NC='\033[0m' # No Color

# ============================================================================
# 日志函数
# ============================================================================

# 打印信息消息（蓝色）
log_info() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

# 打印成功消息（绿色）
log_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

# 打印警告消息（黄色）
log_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

# 打印错误消息（红色，输出到 stderr）
log_error() {
    echo -e "${RED}[ERROR]${NC} $1" >&2
}

# ============================================================================
# 工具函数
# ============================================================================

# 检查是否支持颜色输出
# 返回：0=支持，1=不支持
supports_color() {
    if [ -t 1 ]; then
        case "$TERM" in
            *color*|*256*|xterm*) return 0 ;;
            *) return 1 ;;
        esac
    fi
    return 1
}

# 打印分隔线
print_separator() {
    echo "========================================"
}

# 打印带标题的分隔线
print_header() {
    echo ""
    print_separator
    echo -e "${BOLD} $1${NC}"
    print_separator
    echo ""
}

# 打印子标题
print_subheader() {
    echo ""
    echo -e "${CYAN}--- $1 ---${NC}"
}

# 退出并显示错误消息
# 参数 1: 错误消息
# 参数 2: 退出码（可选，默认 1）
exit_with_error() {
    local message="$1"
    local exit_code="${2:-1}"
    log_error "$message"
    exit "$exit_code"
}

# 检查命令是否存在
# 参数 1: 命令名
# 返回：0=存在，1=不存在
command_exists() {
    command -v "$1" >/dev/null 2>&1
}

# 检查是否在项目根目录（通过检查 pom.xml）
is_project_root() {
    [ -f "$(pwd)/pom.xml" ]
}

# 切换到项目根目录
cd_to_project_root() {
    local script_dir
    script_dir="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
    cd "$script_dir/.." || exit 1
}
